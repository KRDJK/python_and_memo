#산술 연산자

print(10 / 4) # 파이썬은 무조건 실수 나눗셈이다. 10과 4는 정수라서 다른 언어의 경우 정수 나눗셈으로 계산함.
print(10 // 4) #정수 나눗셈 (녻 연산이기도 함.)
print(25 % 7) #몫이 3이고 나머지가 4일 때, 나머지를 구하고 싶은 경우.. %로 나머지 연산.
# /과 %는 모든 언어 공통!

print(3 ** 2) #3의 2제곱
print(2 ** 4) #2의 4제곱 (* 하나는 곱셈, **이면 제곱)

n1 = 30
n2 = 8
print(n2 ** 3)
print(n1 % n2)