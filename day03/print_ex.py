
dog = "멍멍이"
cat = "야옹이"

#중간에 띄어쓰기를 안해도 터미널에서는 띄어져서 나옴.
print(dog,cat,'짹짹이', sep='! ') #sep은 변수에 붙여주는건가?? 
                    #끝을 제외하고는 다 붙여주는듯! 아마도..
                    #sep은 내부에 이미 적용된 것(,로 입력하면 sep을 쓰지 않아도 중간에 공백처리가 되는 것)이나, 임의 변경 가능

fruit = '딸기'
food = '짜장면'

print(fruit, end= ' 메롱!\n\n') #end='\n' 자동으로 들어가있음. end=''으로 공백으로 넣으면 줄바꿈을 하지 않음.
print(food)
# ';' 활용
#\n은 엔터, 즉 줄바꿈이다.
